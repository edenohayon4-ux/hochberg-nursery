const cell = (text: string, opts: { header?: boolean; bg?: string; bold?: boolean; align?: any; color?: string } = {}) =>
  new TableCell({
    shading: opts.bg ? { type: ShadingType.CLEAR, color: "auto", fill: opts.bg } : undefined,
    margins: { top: 80, bottom: 80, left: 100, right: 100 },
    children: [
      new Paragraph({
        bidirectional: RTL,
        alignment: opts.align ?? AlignmentType.CENTER,
        children: [
          new TextRun({
            text,
            font: FONT,
            size: 20,
            bold: opts.header || opts.bold,
            color: opts.color || (opts.header ? "FFFFFF" : "000000"),
            rightToLeft: true,
          }),
        ],
      }),
    ],
  });

const buildTable = (headers: string[], rows: string[][], opts: { headerColor?: string; altRowColor?: string; lastRowBold?: boolean } = {}) => {
  const headerColor = opts.headerColor ?? "2E75B6";
  const altColor = opts.altRowColor ?? "EAF2F9";
  return new Table({
    width: { size: 100, type: WidthType.PERCENTAGE },
    visuallyRightToLeft: true,
    rows: [
      new TableRow({
        tableHeader: true,
        children: headers.map((h) => cell(h, { header: true, bg: headerColor })),
      }),
      ...rows.map(
        (r, i) =>
          new TableRow({
            children: r.map((c) =>
              cell(c, {
                bg: i % 2 === 1 ? altColor : undefined,
                bold: opts.lastRowBold && i === rows.length - 1,
              })
            ),
          })
      ),
    ],
  });
};

